using System;
namespace DataType
{
    class Example
    {
        public static void Main(string[] args)
        {
            int a = 2;
            int b = 3;
            if (a >= b)
            {
                Console.WriteLine("Yes");
            }
            else
            {
                Console.WriteLine("No");
            }


            if (a != b)
            {
                Console.WriteLine("Yes");
            }
            else
            {
                Console.WriteLine("No");
            }


            if (a == b)
            {
                Console.WriteLine("Yes");
            }
            else
            {
                Console.WriteLine("No");
            }
        }
    }
}
    
